task autonomous()
{
	startTask(slewRate);
	startTask(datalog);
	moveStraight(1,1.5);
	wait1Msec(500);
	moveStraight(-1,1.5);
	wait1Msec(500);
	stopTask(slewRate);
	stopTask(datalog);
}



